def load_noisy_data(threshold: float=0.9):
    noisy = []
    for exemplar in exemplars:
        _exemplar = deepcopy(exemplar)
        for i in range(len(_exemplar)):
            proba = np.random.random()
            if proba < threshold:
                continue
            else:
                _exemplar[i] *= 0

        # Remove label from noisy input
        unlabeled_exemplar = _exemplar.reshape(10, 12)
        unlabeled_exemplar[:, -2:] = 0
        unlabeled_exemplar
        noisy += [unlabeled_exemplar.flatten()]

    return noisy